screen_buffer = bytearray(range(960))
cursor_x = 0
cursor_y = 0

def initialize_screen():
    epaper.initialize_display()
    clear_screen()
    flip_page()

def clear_screen():
    global screen_buffer
    set_cursor(0, 0)
    for i in range(0, 960):
        screen_buffer[i] = 0

def set_cursor(x, y):
    global cursor_x, cursor_y
    if x >= 0 and x < 30 and y >= 0 and y < 32:
        cursor_x, cursor_y = x, y
        return True
    else:
        return False

def cursor_advance():
    global cursor_x, cursor_y
    cursor_x = cursor_x + 1
    if cursor_x > 29:
        cursor_x = 0
        cursor_y = cursor_y + 1
        if cursor_y > 31:
            cursor_y = 0

def get_position():
    global cursor_x, cursor_y
    return (cursor_x + (cursor_y * 30))

def add_text(text):
    global screen_buffer
    for element in text:
        element_value = ord(element)
        if element_value > 255 or element_value < 0:
            element_value = 0
        screen_buffer[get_position()] = element_value
        cursor_advance()
        
def add_textvals(text):
    global screen_buffer
    for element_value in text:
        if element_value > 255 or element_value < 0:
            element_value = 0
        screen_buffer[get_position()] = element_value
        cursor_advance()

def crlf():
    return set_cursor(0, cursor_y + 1)

