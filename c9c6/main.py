ui_timer = 950 # Start shortly before the first refresh, so the screen isn't blank for a full minute after boot, but give a few seconds for some initial traffic to potentially come in.
ui_refresh_time = 1000

def ui_handler():
    global ui_timer, ui_refresh_time
    ui_timer += 1
    if ui_timer > ui_refresh_time:
        if DEBUG == True: print('Screen tick starting.')
        ui_timer = 0
        clear_screen()
        set_cursor(0,0)
        add_text("* CYPHERCON 6.0 + 9.0 = 15.0 *")
        for i in range(0,8):
            entry_count, entry_event_id, entry_to_index, entry_from_index, entry_text = read_queue_entry(1,i)
            set_cursor(0,4*i+1)
            add_text("FROM: ")
            add_textvals(read_alias_memory(entry_from_index))
            add_text("(" + str(entry_from_index) + ")")
            crlf()
            add_text("TO:   ")
            add_textvals(read_alias_memory(entry_to_index))
            add_text("(" + str(entry_to_index) + ")")
            crlf()
            if entry_event_id == 3:
                add_text("PAGE: ")
            elif entry_event_id == 4:
                add_text("CAST: ")
            else:
                add_text("????: ")
            add_textvals(entry_text)
        flip_page()
        if DEBUG == True: print('Screen tick complete.')

