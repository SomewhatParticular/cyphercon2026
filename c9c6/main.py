def rx(): # Pull new data, if any, into rx_buffer
    global rx_buffer, RPC, counter
    new_data = False
    start = counter[RPC]
    while(uart0.any() > 0):
        counter_apply_delta(RPC, 1)
        rx_buffer[counter[RPC]] = int.from_bytes(uart0.read(1), 'big')
        new_data = True
    if new_data:
        #if DEBUG == True: print('network: new_data: ', end = '')
        start = start + 1
        if start >= 48:
            start = 0
        while True:
            #if DEBUG == True: print(str(rx_buffer[start]), end = ' ')
            if start == counter[RPC]:
                break
            else:
                start = start + 1
                if start >= 48:
                    start = 0
        #if DEBUG == True: print('')
    return new_data

